package OctEvening;

public class SumOfFirst15 {
public static void main(String[] args) {
	

	int add=0;
	for(int i=1; i<=15; i++)
	{
		
		add=add+i;
	}
	System.out.println(add);
	
	
	//While Loop :
	int sum = 0; 
	int i = 0; // i is 0 Here 
	while (i<=15) 
	{ 
		sum += i;  // i=1 
		i++; }
	System. out. println("The sum is " + sum);//16 Sept 2012

}
}
